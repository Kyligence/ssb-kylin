D_LASTDAYINMONTHFL INT,
D_HOLIDAYFL        INT,
D_WEEKDAYFL        INT)
ROW FORMAT DELIMITED FIELDS TERMINATED BY '|'
STORED AS TEXTFILE
LOCATION '<hdfs-dir>/data/date';
              
