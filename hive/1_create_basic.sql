CREATE DATABASE <DATABASE>;

USE <DATABASE>;

CREATE TABLE <DATABASE>.CUSTOMER ( 
C_CUSTKEY     INT,
C_NAME        string,
C_ADDRESS     string,
C_CITY        string,
C_NATION      string,
C_REGION      string,
C_PHONE       string,
C_MKTSEGMENT   string)
ROW FORMAT DELIMITED FIELDS TERMINATED BY '|'
STORED AS TEXTFILE
LOCATION '<hdfs-dir>/data/customer';

